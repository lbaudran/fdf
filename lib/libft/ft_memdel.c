#include "libft.h"

void		ft_memdel(void **ap)
{
	free(*ap);
	*ap = NULL;
}

int			free_2d_arr(char **arr)
{
	int		i;

	i = 0;
	while (arr[i])
	{
		free(arr[i]);
		arr[i] = NULL;
		i++;
	}
	free(arr);
	arr = NULL;
	return (0);
}
